public class Notas {

    public String nomeMateria;
    public Double av1;
    public Double av2;
    public Double av3;

    public Notas() {
        this.nomeMateria = nomeMateria;
        this.av1 = av1;
        this.av2 = av2;
        this.av3 = av3;
    }

    public String getNomeMateria() {
        return nomeMateria;
    }

    public String setNomeMateria(String nomeMateria) {
        this.nomeMateria = nomeMateria;
        return nomeMateria;
    }

    public Double getAv1() {
        return av1;
    }

    public Double setAv1(Double av1) {
        this.av1 = av1;
        return av1;
    }

    public Double getAv2() {
        return av2;
    }

    public Double setAv2(Double av2) {
        this.av2 = av2;
        return av2;
    }

    public Double getAv3() {
        return av3;
    }

    public Double setAv3(Double av3) {
        this.av3 = av3;
        return av3;
    }
}
